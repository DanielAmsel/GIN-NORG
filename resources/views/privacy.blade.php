                        <li>wenn Sie Widerspruch gegen die Verarbeitung gemäß <a
                                href="https://dsgvo-gesetz.de/art-21-dsgvo/" target="_blank" rel="noopener">Art. 21 Abs.
                                1 DSGVO</a> eingelegt haben
                            und noch nicht feststeht, ob die berechtigten Gründe des Verantwortlichen gegenüber Ihren
                            Gründen überwiegen.</li>
                    </ol>
                    <p>Wurde die Verarbeitung der Sie betreffenden personenbezogenen Daten eingeschränkt, dürfen
                        diese Daten – von ihrer Speicherung abgesehen – nur mit Ihrer Einwilligung oder zur
                        Geltendmachung, Ausübung oder Verteidigung von Rechtsansprüchen oder zum Schutz der
                        Rechte einer anderen natürlichen oder juristischen Person oder aus Gründen eines wichtigen
                        öffentlichen Interesses der Union oder eines Mitgliedstaats verarbeitet werden.
                    </p>
                    <p>Wurde die Verarbeitung nach den o.g. Voraussetzungen eingeschränkt, werden Sie von dem
                        Verantwortlichen unterrichtet, bevor die Einschränkung aufgehoben wird.
                    </p>
                    <p>Bei einer Datenverarbeitung zu wissenschaftlichen, historischen oder statistischen
                        Forschungszwecken kann Ihr Recht auf Einschränkung der Verarbeitung insoweit beschränkt
                        werden, als es voraussichtlich die Verwirklichung der Forschungs- oder Statistikzwecke
                        unmöglich macht oder ernsthaft beeinträchtigt und die Beschränkung für die Erfüllung der
                        Forschungs- oder Statistikzwecke notwendig ist.
                    </p>
                    <h5>5. Recht auf Unterrichtung</h5>
                    <p>Haben Sie das Recht auf Berichtigung, Löschung oder Einschränkung der Verarbeitung
                        gegenüber dem Verantwortlichen geltend gemacht, ist dieser verpflichtet, allen Empfängern,
                        denen die Sie betreffenden personenbezogenen Daten offengelegt wurden, diese Berichtigung
                        oder Löschung der Daten oder Einschränkung der Verarbeitung mitzuteilen, es sei denn, dies
                        erweist sich als unmöglich oder ist mit einem unverhältnismäßigen Aufwand verbunden.
                    </p>
                    <p>Ihnen steht gegenüber dem Verantwortlichen das Recht zu, über diese Empfänger unterrichtet
                        zu werden.
                    </p>
                    <h5>6. Recht auf Datenübertragbarkeit</h5>
                    <p>Sie haben das Recht, die Sie betreffenden personenbezogenen Daten, die Sie dem
                        Verantwortlichen bereitgestellt haben, in einem strukturierten, gängigen und
                        maschinenlesbaren Format zu erhalten. Außerdem haben Sie das Recht diese Daten einem
                        anderen Verantwortlichen ohne Behinderung durch den Verantwortlichen, dem die
                        personenbezogenen Daten bereitgestellt wurden, zu übermitteln, sofern
                    </p>
                    <ol>
                        <li>die Verarbeitung auf einer Einwilligung gem. <a href="https://dsgvo-gesetz.de/art-6-dsgvo/"
                                target="_blank" rel="noopener">Art. 6 Abs. 1 lit. a) DSGVO</a> oder
                            <a href="https://dsgvo-gesetz.de/art-9-dsgvo/" target="_blank" rel="noopener">Art. 9 Abs. 2
                                lit. a) DSGVO</a> oder auf einem Vertrag gem.
                            <a href="https://dsgvo-gesetz.de/art-6-dsgvo/" target="_blank" rel="noopener">Art. 6 Abs. 1
                                lit. b) DSGVO</a> beruht und
                        </li>
                        <li>die Verarbeitung mithilfe automatisierter Verfahren erfolgt.</li>
                    </ol>
                    <p>In Ausübung dieses Rechts haben Sie ferner das Recht, zu erwirken, dass die Sie betreffenden
                        personenbezogenen Daten direkt von einem Verantwortlichen einem anderen
                        Verantwortlichen übermittelt werden, soweit dies technisch machbar ist. Freiheiten und
                        Rechte anderer Personen dürfen hierdurch nicht beeinträchtigt werden.
                    </p>
                    <p>Das Recht auf Datenübertragbarkeit gilt nicht für eine Verarbeitung personenbezogener
                        Daten, die für die Wahrnehmung einer Aufgabe erforderlich ist, die im öffentlichen Interesse
                        liegt oder in Ausübung öffentlicher Gewalt erfolgt, die dem Verantwortlichen übertragen
                        wurde.
                    </p>
                    <h5>7. Widerspruchsrecht</h5>
                    <p>Sie haben das Recht, aus Gründen, die sich aus ihrer besonderen Situation ergeben, jederzeit
                        gegen die Verarbeitung der Sie betreffenden personenbezogenen Daten, die aufgrund von
                        <a href="https://dsgvo-gesetz.de/art-6-dsgvo/" target="_blank" rel="noopener">Art. 6 Abs. 1 lit.
                            e) DSGVO</a> erfolgt, Widerspruch einzulegen; dies gilt auch für ein etwaiges auf
                        diese Bestimmungen gestütztes Profiling.
                    </p>
                    <p>Im Falle des Widerspruchs verarbeitet der Verantwortliche die Sie betreffenden
                        personenbezogenen Daten nicht mehr, es sei denn, er kann zwingende schutzwürdige Gründe
                        für die Verarbeitung nachweisen, die Ihre Interessen, Rechte und Freiheiten überwiegen, oder
                        die Verarbeitung dient der Geltendmachung, Ausübung oder Verteidigung von
                        Rechtsansprüchen.
                    </p>
                    <p>Bei einer Datenverarbeitung zu wissenschaftlichen, historischen oder statistischen
                        Forschungszwecken gemäß <a href="https://dsgvo-gesetz.de/art-89-dsgvo/" target="_blank"
                            rel="noopener">Art. 89 Abs. 1 DSGVO</a> haben Sie zudem das Recht, aus Gründen,
                        die sich aus Ihrer besonderen Situation ergeben, der Verarbeitung Sie betreffender
                        personenbezogener Datenzu widersprechen, es sei denn, die Verarbeitung ist zur Erfüllung
                        einer im öffentlichen Interesse liegenden Aufgabe erforderlich.
                    </p>
                    <h5>8. Recht auf Widerruf der datenschutzrechtlichen Einwilligungserklärung</h5>
                    <p>Sie haben das Recht, Ihre datenschutzrechtliche Einwilligungserklärung jederzeit zu
                        widerrufen. Durch den Widerruf der Einwilligung wird die Rechtmäßigkeit der aufgrund der
                        Einwilligung bis zum Widerruf erfolgten Verarbeitung nicht berührt.
                    </p>
                    <h5>9. Automatisierte Entscheidung im Einzelfall einschließlich Profiling</h5>
                    <p>Sie haben das Recht, nicht einer ausschließlich auf einer automatisierten Verarbeitung –
                        einschließlich Profiling – beruhenden Entscheidung unterworfen zu werden, die Ihnen
                        gegenüber rechtliche Wirkung entfaltet oder Sie in ähnlicher Weise erheblich beeinträchtigt.
                    </p>
                    <p>Dies gilt nicht, wenn die Entscheidung</p>
                    <ol>
                        <li>für den Abschluss oder die Erfüllung eines Vertrags zwischen Ihnen und dem
                            Verantwortlichen erforderlich ist,</li>
                        <li>aufgrund von Rechtsvorschriften der Union oder der Mitgliedstaaten, denen der
                            Verantwortliche unterliegt, zulässig ist und diese Rechtsvorschriften angemessene
                            Maßnahmen zur Wahrung Ihrer Rechte und Freiheiten sowie Ihrer berechtigten Interessen
                            enthalten oder
                        </li>
                        <li>mit Ihrer ausdrücklichen Einwilligung erfolgt.</li>
                    </ol>
                    <p>Allerdings dürfen diese Entscheidungen nicht auf besonderen Kategorien personenbezogener
                        Daten nach <a href="https://dsgvo-gesetz.de/art-9-dsgvo/" target="_blank" rel="noopener">Art. 9
                            Abs. 1 DSGVO</a> beruhen, sofern nicht
                        <a href="https://dsgvo-gesetz.de/art-9-dsgvo/" target="_blank" rel="noopener">Art. 9 Abs. 2 lit.
                            a) oder g) DSGVO</a> gilt und angemessene Maßnahmen zum Schutz der Rechte und Freiheiten sowie
                        Ihrer
                        berechtigten Interessen getroffen wurden.
                    </p>
                    <p>Hinsichtlich der in (1) und (3) genannten Fälle trifft der Verantwortliche angemessene
                        Maßnahmen, um die Rechte und Freiheiten sowie Ihre berechtigten Interessen zu wahren,
                        wozu mindestens das Recht auf Erwirkung des Eingreifens einer Person seitens des
                        Verantwortlichen, auf Darlegung des eigenen Standpunkts und auf Anfechtung der
                        Entscheidung gehört.
                    </p>
